package practice;

public class C75_WhileLoop {
    public static void main(String[] args) {

        int sayi =7;
        int sayac=1;


        while(sayac<11){

            int carpim = sayi*sayac;
            System.out.print(sayi+"*"+sayac+ "=" + carpim + " ");
            sayac++;

        }
    }
}
